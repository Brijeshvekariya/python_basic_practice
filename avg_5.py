def average():
    n1 = int(input("Enter first Number : "))
    n2 = int(input("Enter Second Number : "))
    n3 = int(input("Enter Third Number : "))
    n4 = int(input("Enter Fourth Number : "))
    n5 = int(input("Enter Fifth Number : "))
    avg = n1+n2+n3+n4+n5
    return 'The Average of 5 Number is ',avg/5

print(average())
